
// push : add element to last index
// unshift : add element to first  index




let names =['lion','elephant','tigar','lizard','monkey']
names.push('pork','cow');
console.log(names); //(7) ['lion', 'elephant', 'tigar', 'lizard', 'monkey', 'pork', 'cow']


let animals =['lion','elephant','tigar','lizard','monkey']
animals.unshift('pork','cow');

console.log(animals);  //(7) ['pork', 'cow', 'lion', 'elephant', 'tigar', 'lizard', 'monkey']
