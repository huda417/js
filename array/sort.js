 // sort  ترتتب العناصر تريب ابجدى
 

 let x = ['huda', 'ahmed','sara','ali','gamel']
 console.log(x.sort());
 //(5) ['ahmed', 'ali', 'gamel', 'huda', 'sara']


 let letter = ['e','f','d','b','a','c']
 console.log(letter.sort());
 //(6) ['a', 'b', 'c', 'd', 'e', 'f']

 let num =['2','9','1','7','3','5','4','6','8']
 console.log(num.sort());
 //(9) ['1', '2', '3', '4', '5', '6', '7', '8', '9']
