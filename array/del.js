//shift : Removes the first element from an array
// pop : Removes the last element from an array



let names =['lion','elephant','tigar','lizard','monkey']
names.shift();  //remove  0 lion
names.shift();  //remove  1 elephant
names.shift();  //remove  2 tigar

console.log(names);   //Â [ 'lizard', 'monkey'] 
